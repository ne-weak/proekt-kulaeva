let currentIdx = 0;
let scores = { sanguine: 0, choleric: 0, phlegmatic: 0, melancholic: 0 };

function handleAnswer(val) {
    // 1 (Точно да) = 4 балла, 5 (Точно нет) = 0 баллов
    const points = (5 - val); 
    const type = questions[currentIdx].type;
    scores[type] += points;

    currentIdx++;
    updateQuiz();
}

function updateQuiz() {
    if (currentIdx < questions.length) {
        document.getElementById('question-text').innerText = questions[currentIdx].q;
        const progressPercent = (currentIdx / questions.length) * 100;
        document.getElementById('progress').style.width = `${progressPercent}%`;
    } else {
        showResult();
    }
}

function showResult() {
    document.getElementById('quiz-card').classList.add('hidden');
    const resBox = document.getElementById('result-card');
    resBox.classList.remove('hidden');

    const winner = Object.keys(scores).reduce((a, b) => scores[a] > scores[b] ? a : b);
    const data = results[winner];
    
    document.getElementById('result-title').innerText = data.title;
    
    document.getElementById('result-desc').innerHTML = `
        <div class="main-desc" style="margin-bottom: 20px; font-weight: 500;">${data.desc}</div>
        <div class="social-desc" style="padding: 15px; background: #f9fafb; border-left: 4px solid #4f46e5; margin-bottom: 20px;">
            <strong>Влияние на общение:</strong><br>${data.social}
        </div>
        <div style="font-size: 0.85rem; color: #6b7280; border-top: 1px solid #eee; pt: 15px;">
            <em>Учебная справка:</em> Данный результат демонстрирует, как биологические свойства (темперамент) 
            влияют на процесс интеграции личности в систему общественных отношений.
        </div>
    `;
}

